// ─── Merge and write ──────────────────────────────────────────────────────────

const localeMap = {
  'es-AR': esAR,
  'en': en,
  'es': es,
  'es-MX': esMX,
  'pt-BR': ptBR,
};

for (const [locale, newKeys] of Object.entries(localeMap)) {
  const filePath = join(messagesDir, `${locale}.json`);
  const existing = JSON.parse(readFileSync(filePath, 'utf8'));
  const merged = { ...existing, ...newKeys };
  const sorted = Object.fromEntries(Object.entries(merged).sort(([a], [b]) => a.localeCompare(b)));
  writeFileSync(filePath, JSON.stringify(sorted, null, 2) + '\n', 'utf8');
  console.log(`✓ ${locale}.json — added ${Object.keys(newKeys).length} new keys (total: ${Object.keys(sorted).length})`);
}
